    private void saveData(){
        DBConnect dataLink = new DBConnect();
        String pId = id_tf.getText().trim();
        String pFName = fname_tf.getText().trim();
        String pLName = condition_tf.getText().trim();
        String pGender = gender_cb.getSelectedItem().toString().trim();
        String pDOB = dob_ftf.getText().trim();
        String pAddress = street_tf.getText()+", "+suburb_tf.getText()+", "+
                cityState_tf.getText().trim();
        Boolean personalDetails = isPersonalDetailsValid(pId,pFName,pLName,pGender,
                pDOB,pAddress);
        
        Icon pPicture = null;
        String pPhoneNum = phone_tf.getText().trim();
        String pMedCondition = condition_tf.getText().trim();
        String pComments = comments_tf.getText().trim();
        Boolean conditionAndPic = isConditionAndPicValid(pPicture,pPhoneNum,
                pMedCondition,pComments);
        
        String pDate = date_lbl.getText().trim();
        String pWard = ward_tf.getText().trim();
        String pRoom = room_tf.getText().trim();
        String pTime = time_tf.getText().trim();
        Boolean admissionDetails = isAdmissionValid(pDate,pWard,pRoom,pTime);
        //-----------------------------------------------------------------//
        File f = new File(picturePath);
        try {
            FileInputStream fis = new FileInputStream(f);
            ByteArrayOutputStream baos = new ByteArrayOutputStream();
            byte [] buf = new byte[1024];
            for (int readNum; (readNum=fis.read(buf)) != -1;) {
                baos.write(buf,0,readNum);
            }
            pictureData = baos.toByteArray();
        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "No Picture!", "Warning", JOptionPane.ERROR_MESSAGE); 
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "Picture was corrupted", "Warning", JOptionPane.ERROR_MESSAGE);
        }

        if(personalDetails ){//&& conditionAndPic && admissionDetails){
            String insert = "INSERT INTO patient(patient_id,fname,"
                    + "lname,gender,dob,address,phonenum,medcondition,comments,"
                    + "picture,date,ward,room,time) VALUES"
                    + "('"+pId+"','"+pFName+"','"+pLName+"','"+pGender+"','"+pDOB+"',"
                    + "'"+pAddress+"','"+pPhoneNum+"','"+pMedCondition+"','"+pComments+"',"
                    + "'"+pPicture+"','"+pDate+"','"+pWard+"','"+pRoom+"',"
                    + "'"+pTime+"')";
            String pictureInsert = "UPDATE patient SET picture = '"+pictureData+"' WHERE patient_id = '"+pId+"'";
            
            dataLink.updateTable(insert);
            dataLink.updateTable(pictureInsert);
            clearAll();

        }else{
            JOptionPane.showMessageDialog(null, "Data Incomplete", "Warning", JOptionPane.ERROR_MESSAGE); 
        }
    }

