    private boolean isAlpha(String string) {
        boolean result = false;
        char[] chars = string.toCharArray();
        for (char c : chars) {
            result = false;
            if(Character.isLetter(c)) {
                return true;
            }else{
                break;
            }
        }
        return result;
    }
    
    private boolean isNumeric(String string) {
        boolean result = false;
        char[] chars = string.toCharArray();
        for (char c : chars) {
            result = false;
            if(Character.isDigit(c)) {
                return true;
            }else{
                break;
            }
        }
        return result;
    }
